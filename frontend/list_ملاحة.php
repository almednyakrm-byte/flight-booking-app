This code includes the following features:

1. Session validation: Redirects to login.php if the user is not authenticated.
2. Header navigation: Links to index.php, current user info, and logout.
3. Table showing list of records with actions: Edit (link to edit_ملاحة.php?id=X), Delete (AJAX call to backend).
4. 'Add New Item' button linking to create_ملاحة.php.
5. Search bar filtering elements in real-time.
6. AJAX Javascript (Fetch API) fetching list records from '../backend/ملاحة.php' (GET) and DELETE requests.

Note: This code assumes that the backend PHP file (ملاحة.php) is set up to handle GET and DELETE requests and returns the list of records in JSON format.